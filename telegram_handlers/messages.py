# Pending settings handler
# ===========================================================================

async def _handle_pending_setting(
    update: Update,
    context: ContextTypes.DEFAULT_TYPE,
    user_id: int,
    setting_key: str,
    text: str,
) -> None:
    """Handle input for a pending setting (e.g. number of shorts)."""
    if setting_key == "num_shorts":
        try:
            count = int(text)
            if not 1 <= count <= 20:
                raise ValueError("Out of range")
            await settings_db.update_num_shorts(user_id, count)
            await update.message.reply_text(
                f"✅ Number of Shorts set to *{count}*.",
                reply_markup=main_menu_keyboard(),
                parse_mode="Markdown",
            )
        except (ValueError, TypeError):
            await update.message.reply_text(
                "❌ Please enter a number between 1 and 20.",
                parse_mode="Markdown",
            )
            return

    # Clear pending setting
    context.user_data.pop("pending_setting", None)

    from telegram_handlers.common import log_setting_change
    await log_setting_change(user_id, setting_key, text)
