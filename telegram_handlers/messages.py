# ===========================================================================
# Text message handler (YouTube URLs + settings input)
# ===========================================================================

async def handle_text_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    Handle incoming text messages.

    - If the text contains a YouTube URL, start processing.
    - If the user has a pending setting (e.g. num_shorts), process the input.
    - Otherwise, show the main menu.
    """
    user = update.effective_user
    if not user:
        return

    # Rate limit check
    if rate_limiter.is_rate_limited(user.id):
        remaining = rate_limiter.get_reset_seconds(user.id)
        await update.message.reply_text(
            f"⚠️ Rate limited. Try again in {remaining:.0f}s."
        )
        return

    # Ensure user exists in DB
    await users_db.upsert_user(
        user_id=user.id,
        username=user.username,
        first_name=user.first_name,
        last_name=user.last_name,
        language_code=user.language_code or "en",
    )

    text = update.message.text or ""
    text = sanitise_text(text)

    # Check for pending setting input
    pending_setting = context.user_data.get("pending_setting")
    if pending_setting:
        await _handle_pending_setting(update, context, user.id, pending_setting, text)
        return

    # Check for YouTube URL
    if _contains_youtube_url(text):
        await _handle_youtube_url(update, context, user.id, text)
        return

    # Default: show main menu
    await update.message.reply_text(
        "👋 Send me a *YouTube URL* or *upload a video* to create Shorts!\n\n"
        "Use the menu below for other options:",
        reply_markup=main_menu_keyboard(),
        parse_mode="Markdown",
    )


async def _contains_youtube_url(text: str) -> bool:
    """Check if the text contains a YouTube URL."""
    youtube_patterns = [
        "youtube.com/watch?v=",
        "youtu.be/",
        "youtube.com/shorts/",
        "youtube.com/live/",
        "youtube.com/playlist",
    ]
    text_lower = text.lower()
    return any(pattern in text_lower for pattern in youtube_patterns)


async def _handle_youtube_url(
    update: Update,
    context: ContextTypes.DEFAULT_TYPE,
    user_id: int,
    text: str,
) -> None:
    """Process a YouTube URL submission."""
    rate_limiter.register_request(user_id)

    # Validate URL
    is_valid, reason = validate_youtube_url(text)
    if not is_valid:
        await update.message.reply_text(f"❌ {reason}")
        return

    # Log the event
    await stats_db.log_event(
        event_type="video_downloaded",
        user_id=user_id,
        details={"url": text[:200], "source": "youtube"},
    )

    # Get user settings for the job
    settings = await settings_db.get_settings_for_job(user_id)

    # Acknowledge receipt
    status_msg = await update.message.reply_text(
        "📥 *Processing YouTube Video...*\n\n"
        "Downloading and analysing your video. "
        "This may take a few minutes.\n\n"
        "You'll receive updates as processing progresses.",
        parse_mode="Markdown",
    )

    # Create and submit job
    job = QueueJob(
        job_id="",  # Will be generated by create_job
        user_id=user_id,
        source_type="youtube",
        source_url=text,
        settings=settings,
    )

    # Create DB record first to get the job_id
    from database import jobs as jobs_db
    job_record = await jobs_db.create_job(
        user_id=user_id,
        source_type="youtube",
        source_url=text,
        num_shorts=settings.get("num_shorts", 3),
        settings_snapshot=settings,
    )
    job.job_id = job_record["job_id"]

    # Register progress callback
    async def on_progress(progress: int, message: str) -> None:
        try:
            await status_msg.edit_text(
                f"📥 *Processing...*\n\n"
                f"📊 Progress: {progress}%\n"
                f"📝 {message}",
                parse_mode="Markdown",
            )
        except Exception:
            pass  # Message may have been edited already

    queue_manager.register_progress_callback(job.job_id, on_progress)

    # Submit to queue
    # Override submit_job since we already created the DB record
    await queue_manager._queue.put(job)
    await stats_db.log_event(
        event_type="job_created",
        user_id=user_id,
        details={"job_id": job.job_id, "source_type": "youtube"},
    )

    logger.info("YouTube job %s submitted for user %d", job.job_id[:8], user_id)


