# Document / file upload handler
# ===========================================================================

async def handle_document_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    Handle document (file) uploads.

    Validates the file, downloads it, and submits a processing job.
    """
    user = update.effective_user
    if not user:
        return

    doc = update.message.document
    if not doc:
        return

    # Rate limit
    if rate_limiter.is_rate_limited(user.id):
        remaining = rate_limiter.get_reset_seconds(user.id)
        await update.message.reply_text(
            f"⚠️ Rate limited. Try again in {remaining:.0f}s."
        )
        return

    rate_limiter.register_request(user.id)

    # Validate
    is_valid, reason = validate_uploaded_video(update.message)
    if not is_valid:
        await update.message.reply_text(f"❌ {reason}")
        return

    # Ensure user exists
    await users_db.upsert_user(
        user_id=user.id,
        username=user.username,
        first_name=user.first_name,
        last_name=user.last_name,
        language_code=user.language_code or "en",
    )

    # Download file
    file_name = sanitise_filename(doc.file_name or "video.mp4")
    file_path = UPLOADS_DIR / f"{user.id}_{file_name}"

    status_msg = await update.message.reply_text(
        "📥 *Downloading your file...*\n\n"
        "This may take a moment depending on the file size.",
        parse_mode="Markdown",
    )

    try:
        file = await context.bot.get_file(doc.file_id)
        await file.download_to_drive(str(file_path))
    except Exception as e:
        logger.error("Failed to download file for user %d: %s", user.id, e)
        await status_msg.edit_text(f"❌ Failed to download file: {e}")
        return

    # Log upload event
    await stats_db.log_event(
        event_type="file_uploaded",
        user_id=user.id,
        details={"filename": file_name, "size": doc.file_size or 0},
    )

    # Get settings
    settings = await settings_db.get_settings_for_job(user.id)

    # Create job
    job_record = await jobs_db.create_job(
        user_id=user.id,
        source_type="upload",
        source_file_path=str(file_path),
        source_filename=file_name,
        num_shorts=settings.get("num_shorts", 3),
        settings_snapshot=settings,
    )

    # Update status
    await status_msg.edit_text(
        "📥 *File Received!*\n\n"
        f"*File:* `{file_name}`\n"
        f"*Size:* {(doc.file_size or 0) / (1024*1024):.1f} MB\n\n"
        "Processing will begin shortly. "
        "You'll receive updates as progress is made.",
        parse_mode="Markdown",
    )

    # Create job object and submit
    job = QueueJob(
        job_id=job_record["job_id"],
        user_id=user.id,
        source_type="upload",
        source_file_path=str(file_path),
        source_filename=file_name,
        settings=settings,
    )

    async def on_progress(progress: int, message: str) -> None:
        try:
            await status_msg.edit_text(
                f"📥 *Processing...*\n\n"
                f"📊 Progress: {progress}%\n"
                f"📝 {message}",
                parse_mode="Markdown",
            )
        except Exception:
            pass

    queue_manager.register_progress_callback(job.job_id, on_progress)
    await queue_manager._queue.put(job)

    logger.info("Upload job %s submitted for user %d", job.job_id[:8], user.id)


